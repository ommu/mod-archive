use yii\helpers\Html;
use yii\helpers\Url;
use yii\widgets\DetailView;

if (!$small) {
    $context = $this->context;
    if ($context->breadcrumbApp) {
        $this->params['breadcrumbs'][] = ['label' => $context->breadcrumbAppParam['name'], 'url' => [$context->breadcrumbAppParam['url']]];
    }

    $this->params['breadcrumbs'][] = ['label' => Yii::t('app', 'Restoration'), 'url' => ['index']];
    $this->params['breadcrumbs'][] = $model->archive->code;

    $this->params['menu']['content'] = [
        ['label' => Yii::t('app', 'Update'), 'url' => Url::to(['update', 'id' => $model->id]), 'icon' => 'pencil', 'htmlOptions' => ['class' => 'btn btn-primary']],
        ['label' => Yii::t('app', 'Delete'), 'url' => Url::to(['delete', 'id' => $model->id]), 'htmlOptions' => ['data-confirm' => Yii::t('app', 'Are you sure you want to delete this item?'), 'data-method' => 'post', 'class' => 'btn btn-danger'], 'icon' => 'trash'],
    ];
} ?>

<div class="archive-restoration-view">

<?php
$attributes = [
	[
		'attribute' => 'id',
		'value' => $model->id ? $model->id : '-',
		'visible' => !$small,
	],
	[
		'attribute' => 'archiveTitle',
		'value' => function ($model) {
            $archiveTitle = isset($model->archive) ? $model->archive->title : '-';
            if ($archiveTitle != '-') {
                return Html::a($archiveTitle, ['admin/view', 'id' => $model->archive_id], ['title' => $archiveTitle, 'class' => 'modal-btn']);
            }
            return $archiveTitle;
		},
		'format' => 'html',
	],
	[
		'attribute' => 'archiveCode',
		'value' => function ($model) {
            $archiveCode = isset($model->archive) ? $model->archive->code : '-';
            return $archiveCode;
		},
		'format' => 'html',
	],
	[
		'attribute' => 'condition',
		'value' => $model::getCondition($model->condition),
		'visible' => !$small,
	],
	[
		'attribute' => 'condition_date',
		'value' => Yii::$app->formatter->asDatetime($model->condition_date, 'medium'),
		'visible' => !$small,
	],
	[
		'attribute' => 'creation_date',
		'value' => Yii::$app->formatter->asDatetime($model->creation_date, 'medium'),
		'visible' => !$small,
	],
	[
		'attribute' => 'creationDisplayname',
		'value' => isset($model->creation) ? $model->creation->displayname : '-',
		'visible' => !$small,
	],
	[
		'attribute' => 'modified_date',
		'value' => Yii::$app->formatter->asDatetime($model->modified_date, 'medium'),
		'visible' => !$small,
	],
	[
		'attribute' => 'modifiedDisplayname',
		'value' => isset($model->modified) ? $model->modified->displayname : '-',
		'visible' => !$small,
	],
	[
		'attribute' => '',
		'value' => Html::a(Yii::t('app', 'Update Status'), ['update', 'id' => $model->primaryKey], ['title' => Yii::t('app', 'Update Status'), 'class' => 'btn btn-primary btn-sm modal-btn']),
		'format' => 'html',
		'visible' => !$small && Yii::$app->request->isAjax ? true : false,
	],
];

echo DetailView::widget([
	'model' => $model,
	'options' => [
		'class' => 'table table-striped detail-view',
	],
	'attributes' => $attributes,
]); ?>

</div>